package com.interviewprep.domain;

import jakarta.persistence.*;
import org.hibernate.annotations.JdbcTypeCode;
import org.hibernate.type.SqlTypes;
import java.time.LocalDateTime;

/**
 * JPA entity representing an AI-generated evaluation of a user's answer within an interview session.
 * <p>
 * Each {@code Evaluation} is linked to exactly one {@link Answer} and contains:
 * <ul>
 *   <li>A numeric score (0-100) reflecting the quality of the answer</li>
 *   <li>JSONB-formatted arrays of strengths and weaknesses identified by the AI evaluator</li>
 *   <li>An improved version of the answer generated by the AI</li>
 *   <li>A correctness assessment with explanation</li>
 *   <li>Time and space complexity analysis of the solution</li>
 *   <li>Status tracking via {@link EvaluationStatus}</li>
 * </ul>
 * <p>
 * Evaluations are created asynchronously by the AI evaluation pipeline after a user submits answers.
 * The JSONB fields ({@code strengthsJson}, {@code weaknessesJson}) store arrays of strings that describe
 * what the answer did well and where it fell short, parsed at runtime via Jackson deserialization.
 * </p>
 * <p>
 * Table: {@code evaluations}
 * </p>
 *
 * @see Answer
 * @see EvaluationStatus
 * @see EvaluationRepository
 */
@Entity
@Table(name = "evaluations")
public class Evaluation {

    /** Unique identifier for this evaluation record. Auto-generated by the database. */
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    /** The answer being evaluated. Lazy-loaded to avoid unnecessary joins when only summary data is needed. */
    @ManyToOne(fetch = FetchType.LAZY)
    @JoinColumn(name = "answer_id", nullable = false)
    private Answer answer;

    /** Numeric score (0-100) assigned by the AI evaluator. Null until evaluation completes. */
    private Integer score;

    /** JSONB array of strings describing what the answer did well. Parsed via Jackson at runtime. */
    @JdbcTypeCode(SqlTypes.JSON)
    @Column(columnDefinition = "jsonb")
    private String strengthsJson;

    /** JSONB array of strings describing areas for improvement identified by the AI evaluator. */
    @JdbcTypeCode(SqlTypes.JSON)
    @Column(columnDefinition = "jsonb")
    private String weaknessesJson;

    /** A complete improved version of the answer, generated by the AI for reference and learning. */
    @Column(columnDefinition = "text")
    private String improvedAnswer;

    /** Whether the evaluated answer is factually correct according to the AI assessment. Defaults to false. */
    @Column(name = "is_correct", columnDefinition = "boolean default false")
    private Boolean isCorrect;

    /** Detailed explanation of why the answer was judged correct or incorrect, provided by the AI evaluator. */
    @Column(name = "correctness_explanation", columnDefinition = "text")
    private String correctnessExplanation;

    /** Time complexity analysis (e.g., "O(n log n)") of the evaluated solution, as determined by the AI. */
    private String timeComplexity;

    /** Space complexity analysis (e.g., "O(1)") of the evaluated solution, as determined by the AI. */
    private String spaceComplexity;

    /** Current status of this evaluation, tracked via {@link EvaluationStatus} enum values. */
    @Enumerated(EnumType.STRING)
    @Column(nullable = false)
    private EvaluationStatus status;

    /** Default no-arg constructor required by JPA for entity instantiation. */
    public Evaluation() {}

    public Long getId() { return id; }
    public void setId(Long id) { this.id = id; }
    public Answer getAnswer() { return answer; }
    public void setAnswer(Answer answer) { this.answer = answer; }
    public Integer getScore() { return score; }
    public void setScore(Integer score) { this.score = score; }
    public String getStrengthsJson() { return strengthsJson; }
    public void setStrengthsJson(String strengthsJson) { this.strengthsJson = strengthsJson; }
    public String getWeaknessesJson() { return weaknessesJson; }
    public void setWeaknessesJson(String weaknessesJson) { this.weaknessesJson = weaknessesJson; }
    public String getImprovedAnswer() { return improvedAnswer; }
    public void setImprovedAnswer(String improvedAnswer) { this.improvedAnswer = improvedAnswer; }
    public Boolean getIsCorrect() { return isCorrect; }
    public void setIsCorrect(Boolean isCorrect) { this.isCorrect = isCorrect; }
    public String getCorrectnessExplanation() { return correctnessExplanation; }
    public void setCorrectnessExplanation(String correctnessExplanation) { this.correctnessExplanation = correctnessExplanation; }
    public String getTimeComplexity() { return timeComplexity; }
    public void setTimeComplexity(String timeComplexity) { this.timeComplexity = timeComplexity; }
    public String getSpaceComplexity() { return spaceComplexity; }
    public void setSpaceComplexity(String spaceComplexity) { this.spaceComplexity = spaceComplexity; }
    public EvaluationStatus getStatus() { return status; }
    public void setStatus(EvaluationStatus status) { this.status = status; }
}
